package homework_8;

public class Methods {
    private double a;
    private double b;

    public Methods(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double add(){
        return a+b;
    }
    public double subtract(){
        return a-b;
    }
    public double multiply(){
        return a*b;
    }
    public double devide(){
        return a/b;
    }
}
